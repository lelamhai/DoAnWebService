CREATE DATABASE QLSV
GO

USE QLSV
GO

CREATE TABLE LOAINHANVIEN(
	MaLoaiNV int IDENTITY(1,1) NOT NULL PRIMARY KEY,
	TenLoaiNV nvarchar(100) NOT NULL
)
GO

CREATE TABLE NHANVIEN(
	MANV nchar(10) NOT NULL PRIMARY KEY,
	HO nvarchar(50) NOT NULL,
	TEN nvarchar(50) NOT NULL,
	PHAI bit NOT NULL DEFAULT 1,
	DIACHI nvarchar(100) NULL,
	NGAYSINH date NULL,
	EMAIL varchar(50) NULL UNIQUE,
	DANGLAM bit NOT NULL,
	PASSWORD nvarchar(40) NULL,
	MaLoaiNV int NOT NULL,
	CONSTRAINT FK_NHANVIEN_LOAINHANVIEN
        FOREIGN KEY (MaLoaiNV)
        REFERENCES LOAINHANVIEN(MaLoaiNV)
)
GO

CREATE TABLE KHOA(
	MAKHOA nchar(10) NOT NULL PRIMARY KEY,
	TENKHOA nvarchar(50) NOT NULL
)
GO

CREATE TABLE LOP(
	MALOP nchar(10) NOT NULL PRIMARY KEY,
	TENLOP nvarchar(50) NOT NULL,
	KHOAHOC nchar(9) NOT NULL,
	MAKHOA nchar(10) NOT NULL,
	MANV nchar(10) NOT NULL,
	CONSTRAINT FK_LOP_KHOA 
		FOREIGN KEY (MAKHOA) REFERENCES KHOA(MAKHOA),
	CONSTRAINT FK_LOP_NHANVIEN
        FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV)
)
GO

CREATE TABLE SINHVIEN(
	MASV nchar(10) NOT NULL PRIMARY KEY,
	HO nvarchar(50) NOT NULL,
	TEN nvarchar(10) NOT NULL,
	PHAI bit NOT NULL DEFAULT 1,
	DIACHI nvarchar(100) NULL,
	NGAYSINH date NULL,
	EMAIL varchar(50) NULL,
	MALOP nchar(10) NOT NULL,
	DANGHOC bit NOT NULL DEFAULT 1,
	PASSWORD nvarchar(40) NULL,
	CONSTRAINT FK_SINHVIEN_LOP
		FOREIGN KEY (MALOP) REFERENCES LOP(MALOP)
)
GO

CREATE TABLE HOCPHI(
	MASV nchar(10) NOT NULL,
	NIENKHOA nchar(9) NOT NULL,
	HOCKY int NOT NULL,
	HOCPHI int NOT NULL,
	CONSTRAINT PK_HOCPHI PRIMARY KEY (MASV, NIENKHOA, HOCKY),
    CONSTRAINT FK_HOCPHI_SINHVIEN 
		FOREIGN KEY (MASV) REFERENCES SINHVIEN(MASV)
)
GO

CREATE TABLE CT_DONGHOCPHI(
	MASV nchar(10) NOT NULL,
	NIENKHOA nchar(9) NOT NULL,
	HOCKY int NOT NULL,
	NGAYDONG date NOT NULL,
	SOTIENDONG int NOT NULL,
	CONSTRAINT PK_CTHOCPHI PRIMARY KEY (MASV, NIENKHOA, HOCKY, NGAYDONG),
	CONSTRAINT FK_CTDONGHOCPHI_HOCPHI
        FOREIGN KEY (MASV, NIENKHOA, HOCKY)
        REFERENCES HOCPHI(MASV, NIENKHOA, HOCKY)
)
GO

CREATE TABLE GIANGVIEN(
	MAGV nchar(10) NOT NULL PRIMARY KEY,
	MAKHOA nchar(10) NOT NULL,
	HO nvarchar(50) NOT NULL,
	TEN nvarchar(10) NOT NULL,
	HOCVI nvarchar(20) NULL,
	HOCHAM nvarchar(20) NULL,
	CHUYENMON nvarchar(50) NULL,
	EMAIL varchar(50) NULL,
	DANGDAY bit NOT NULL DEFAULT 1,
	PASSWORD nvarchar(40) NULL,
	CONSTRAINT FK_GIANGVIEN_KHOA
		FOREIGN KEY (MAKHOA) REFERENCES KHOA(MAKHOA)
)
GO

CREATE TABLE MONHOC(
	MAMH nchar(10) NOT NULL PRIMARY KEY,
	TENMH nvarchar(50) NOT NULL,
	SOTIET_LT int NOT NULL,
	SOTIET_TH int NOT NULL,
	SOTINCHI int,
	MAKHOA nchar(10) NOT NULL,
	CONSTRAINT FK_MONHOC_KHOA
		FOREIGN KEY (MAKHOA) REFERENCES KHOA(MAKHOA)
)
GO

CREATE TABLE LOPTINCHI(
	MALTC int IDENTITY(1,1) NOT NULL PRIMARY KEY,
	NIENKHOA nchar(9) NOT NULL,
	HOCKY int NOT NULL,
	MAMH nchar(10) NOT NULL,
	NHOM int NOT NULL,
	MAGV nchar(10) NOT NULL,
	SISO_TOIDA int NOT NULL,
	SISO_HIENTAI int NOT NULL,
	DAY_THUTRONGTUAN varchar(50) NULL,
	THOIGIAN_BATDAU date NULL,
	THOIGIAN_KETTHUC date NULL,
	HUYLOP bit NOT NULL DEFAULT 0,
	CONSTRAINT FK_LOPTINCHI_GIANGVIEN
        FOREIGN KEY (MAGV)
        REFERENCES GIANGVIEN(MAGV),
	CONSTRAINT FK_LOPTINCHI_MONHOC
        FOREIGN KEY (MAMH)
        REFERENCES MONHOC(MAMH)
)
GO

CREATE TABLE DANGKY(
	MALTC int NOT NULL,
	MASV nchar(10) NOT NULL,
	DIEM_CC int NULL,
	DIEM_GK float NULL,
	DIEM_CK float NULL,
	XEPLOAI varchar(20) NULL,
	DANGKY bit NULL DEFAULT 1,
	CONSTRAINT PK_DANGKY PRIMARY KEY (MALTC, MASV),
	CONSTRAINT FK_DANGKY_SINHVIEN
        FOREIGN KEY (MASV)
        REFERENCES SINHVIEN(MASV),
	CONSTRAINT FK_DANGKY_LOPTINCHI
        FOREIGN KEY (MALTC)
        REFERENCES LOPTINCHI(MALTC)
)
GO

